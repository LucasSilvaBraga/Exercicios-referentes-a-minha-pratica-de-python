#====ESCREVA UM PROGRAMA QUE LEIA UM VALOR EM METROS E O EXIBA CONVERTIDO EM CENTIMETROS E MILIMETROS====

Tamanho = int(input('Digite um valor em metros: '))

print ('O valor digitado convertido em centimetros eh: {}'. format(Tamanho * 100))

print ('O valor digitado convertido em milimetros eh: {}'. format (Tamanho * 1000))
